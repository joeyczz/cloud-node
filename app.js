const express = require('express');
const path = require('path');
const cookieParser = require('cookie-parser');
const bodyParser = require('body-parser');
// morgan log配置
const morganLog = require('./src/utils/morganLog');

const app = express();

/* logger start */
// 引入 log4j 以及
require('./src/utils/logger');

app.use(morganLog);
/* logger end */

// async 使用
require('express-async-errors');

// 引入mongodb
require('./src/model/mongodb');
// 引入redis
require('./src/model/redis');
// 拦截器
const filterUtil = require('./src/utils/filterUtil');

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

// cros 跨域问题
app.use((req, res, next) => {
  res.header('Access-Control-Allow-Origin', '*');
  res.header('Access-Control-Allow-Methods', 'GET, PUT, POST, DELETE');
  res.header('Access-Control-Allow-Headers', 'Content-Type');
  res.header('Access-Control-Allow-Credentials', 'true');
  next();
});

app.use(filterUtil.filter);

const auth = require('./src/controllers/auth/AuthController');
const user = require('./src/controllers/user/UserController');

app.use('/auth', auth);
app.use('/user', user);

// catch 404 and forward to error handler
app.use((req, res, next) => {
  const err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handler
app.use((err, req, res) => {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
